	byte[] ConvertFromHexString(string input)
	{
		int length = input.Length / 2;
		byte[] bytes = new byte[length];
		char[] numeral = new char[2];
		for (int i = 0; i < length; i++)
		{
			input.CopyTo(i * 2, numeral, 0, 2);
			bytes[i] = System.Convert.ToByte(new string(numeral), 16);
		}
		return bytes;
	}

	string ConvertToHexString(byte[] bytes)
	{
		return System.BitConverter.ToString(bytes).Replace("-","");
	}

	Quaternion LookRotation(Vector3 forward, Vector3 upwards)
	{
		forward.Normalize();
		Vector3 right = Vector3.Cross(upwards, forward).normalized;
		Vector3 up = Vector3.Cross(forward, right);
		float m00 = right.x;
		float m01 = right.y;
		float m02 = right.z;
		float m10 = up.x;
		float m11 = up.y;
		float m12 = up.z;
		float m20 = forward.x;
		float m21 = forward.y;
		float m22 = forward.z;
		float num8 = (m00 + m11) + m22;
		Quaternion quaternion = new Quaternion();
		if (num8 > 0f)
		{
			float num = Mathf.Sqrt(num8 + 1f);
			quaternion.w = num * 0.5f;
			num = 0.5f / num;
			quaternion.x = (m12 - m21) * num;
			quaternion.y = (m20 - m02) * num;
			quaternion.z = (m01 - m10) * num;
			return quaternion;
		}
		if ((m00 >= m11) && (m00 >= m22))
		{
			float num7 = Mathf.Sqrt(((1f + m00) - m11) - m22);
			float num4 = 0.5f / num7;
			quaternion.x = 0.5f * num7;
			quaternion.y = (m01 + m10) * num4;
			quaternion.z = (m02 + m20) * num4;
			quaternion.w = (m12 - m21) * num4;
			return quaternion;
		}
		if (m11 > m22)
		{
			float num6 = Mathf.Sqrt(((1f + m11) - m00) - m22);
			float num3 = 0.5f / num6;
			quaternion.x = (m10 + m01) * num3;
			quaternion.y = 0.5f * num6;
			quaternion.z = (m21 + m12) * num3;
			quaternion.w = (m20 - m02) * num3;
			return quaternion;
		}
		float num5 = Mathf.Sqrt(((1f + m22) - m00) - m11);
		float num2 = 0.5f / num5;
		quaternion.x = (m20 + m02) * num2;
		quaternion.y = (m21 + m12) * num2;
		quaternion.z = 0.5f * num5;
		quaternion.w = (m01 - m10) * num2;
		return quaternion;
	}

	Mesh CreateQuadMesh()
	{
		Mesh mesh = new Mesh();
		mesh.vertices = new Vector3[]
		{
			new Vector3(-0.5f, -0.5f, 0),
			new Vector3( 0.5f, -0.5f, 0),
			new Vector3(-0.5f,  0.5f, 0),
			new Vector3( 0.5f,  0.5f, 0)
		};
		mesh.uv = new Vector2[]
		{
			new Vector2(0, 0),
			new Vector2(1, 0),
			new Vector2(0, 1),
			new Vector2(1, 1)
		};
		mesh.triangles = new int[] {0, 2, 1, 2, 3, 1};
		mesh.RecalculateNormals();
		return mesh;
	}

	void Start()
	{
		_TextureData = _TextureData.Replace(" ", "").Replace("\n", "").Replace("\r", "").Replace("\t", "");
		byte[] bytes = ConvertFromHexString(_TextureData);
		_Texture = new Texture2D(128, 128, TextureFormat.DXT5, true);
		_Texture.LoadRawTextureData(bytes);
		_Texture.Apply();
		_Material = new Material(Shader.Find("Sprites/Default"));
		_Material.enableInstancing = true;
		_Material.mainTexture = _Texture;
		_Mesh = CreateQuadMesh();
		_Positions = new Vector3[Amount];
	}

	void Update()
	{
		Vector3 offset = this.transform.position;
		for (int i = 0; i < Amount; i++)
		{
			float time = Time.time * Speed + 32.0f;
			float index = (float) i + (float) Amount;
			float seedX = Mathf.Sin(index * 127.1f + index * 311.7f) * 43758.5453f;
			float seedY = Mathf.Sin(index * 269.5f + index * 183.3f) * 43758.5453f;
			float hashX = (seedX - Mathf.Floor(seedX)) * 0.5f + 0.5f;
			float hashY = (seedY - Mathf.Floor(seedY)) * 0.5f + 0.5f;
			float spreadX = Spread + hashX * 5.0f;
			float spreadY = Spread + hashY * 5.0f;
			Vector3 position = new Vector3(Mathf.Cos(hashX * time + 1.0f) * spreadX, Height, Mathf.Cos(hashY * time + 1.0f) * spreadY);
			Vector3 direction = position - _Positions[i];
			Quaternion rotation = LookRotation(direction, Vector3.up) * Quaternion.Euler(90f, 0f, 0f);
			_Positions[i] = position;
			Matrix4x4 matrix = Matrix4x4.TRS(position + offset, rotation, Vector3.one);
			Graphics.DrawMesh(_Mesh, matrix, _Material, 0);
		}
	}

	void OnValidate()
	{
		_Positions = new Vector3[Amount];
	}

	void OnDestroy()
	{
		Destroy(_Texture);
		Destroy(_Material);
		Destroy(_Mesh);
	}

